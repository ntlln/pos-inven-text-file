    void retrieveData() {
        DefaultTableModel model = (DefaultTableModel) inventoryTable.getModel();

        String filePath = productStorage.getfilepath();

        try ( BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] rowData = line.split(",");

                model.addRow(rowData);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private double calculateTotal(String productName, int quantity, double price) {
        double total = 0;

        try ( BufferedReader reader = new BufferedReader(new FileReader("data.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] rowData = line.split(",");
                String name = rowData[1].trim();
                int storedQuantity = Integer.parseInt(rowData[2]);
                double storedPrice = Double.parseDouble(rowData[3]);

                if (name.equalsIgnoreCase(productName)) {
                    total += quantity * price;
                }
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }

        return total;
    }

    private void updateProductQuantity(String productName, int quantity) {
        //String databaseFilePath = "path/to/your/database/file.txt";

        try ( BufferedReader reader = new BufferedReader(new FileReader("/Users/allen/NetBeansProjects/pos/src/db/products.txt"))) {
            StringBuilder fileContent = new StringBuilder();
            String line;

            // Read the file line by line
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");

                // Check if the current line corresponds to the product to be updated
                if (parts.length >= 2 && parts[0].equals(productName)) {
                    int currentQuantity = Integer.parseInt(parts[1]);
                    int updatedQuantity = currentQuantity - quantity;
                    parts[1] = String.valueOf(updatedQuantity);
                }

                // Append the updated line to the file content
                fileContent.append(String.join(",", parts)).append(System.lineSeparator());
            }

            // Save the updated content back to the file
            try ( BufferedWriter writer = new BufferedWriter(new FileWriter("/Users/allen/NetBeansProjects/pos/src/db/products.txt"))) {
                writer.write(fileContent.toString());
            }
        } catch (IOException e) {
            // Handle the exception appropriately
            e.printStackTrace();
        }
    }

