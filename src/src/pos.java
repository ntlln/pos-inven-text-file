    private void inventoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inventoryTableMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = inventoryTable.getSelectedRow();

        String productName = inventoryTable.getValueAt(selectedRowIndex, 1).toString();
        double price = Double.parseDouble(inventoryTable.getValueAt(selectedRowIndex, 3).toString());

        int quantity = 1;

        DefaultTableModel model = (DefaultTableModel) itemsOverview.getModel();

        boolean productExists = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            String existingProductName = model.getValueAt(i, 0).toString();
            if (existingProductName.equals(productName)) {
                int existingQuantity = Integer.parseInt(model.getValueAt(i, 1).toString());
                double existingPrice = Double.parseDouble(model.getValueAt(i, 2).toString());
                int updatedQuantity = existingQuantity + quantity;
                double updatedPrice = existingPrice + (price * quantity); // Adjusted calculation
                model.setValueAt(updatedQuantity, i, 1);
                model.setValueAt(updatedPrice, i, 2);
                productExists = true;
                break;
            }
        }

        if (!productExists) {
            model.addRow(new Object[]{productName, quantity, price});
        }

        double subtotal = 0.0;
        for (int i = 0; i < model.getRowCount(); i++) {
            double itemPrice = Double.parseDouble(model.getValueAt(i, 2).toString());
            subtotal += itemPrice;
        }

        double taxRate = 0.10;
        double tax = subtotal * taxRate;

        double totalPrice = subtotal + tax;

        subtotalfield.setText(String.valueOf(subtotal));
        taxfield.setText(String.valueOf(tax));
        totalfield.setText(String.valueOf(totalPrice));
    }//GEN-LAST:event_inventoryTableMouseClicked

    private void paybuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paybuttonActionPerformed
        // TODO add your handling code here:
        String cashAmountText = cashamount.getText();
        if (cashAmountText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No payment received.");
        } else {
            double total = Double.parseDouble(totalfield.getText());
            double cashAmount = Double.parseDouble(cashAmountText);
            double change = cashAmount - total;
            changefield.setText(String.valueOf(change));

            if (cashAmount < total) {
                JOptionPane.showMessageDialog(null, "Invalid amount. The cash amount is less than the total.");
                return;
            }

            JOptionPane pane = new JOptionPane();
            pane.setMessage("Transaction successful.");
            pane.setOptionType(JOptionPane.DEFAULT_OPTION);
            pane.setOptions(new Object[]{"Print Receipt"});
            JDialog dialog = pane.createDialog(null, "Success");
            dialog.setVisible(true);

            Object selectedOption = pane.getValue();
            if (selectedOption != null && selectedOption.equals("Print Receipt")) {
            }
        }
    }//GEN-LAST:event_paybuttonActionPerformed

    private void removebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebuttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = itemsOverview.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) itemsOverview.getModel();

        if (selectedRow != -1) {
            double removedPrice = Double.parseDouble(model.getValueAt(selectedRow, 2).toString());
            int removedQuantity = Integer.parseInt(model.getValueAt(selectedRow, 1).toString());

            model.removeRow(selectedRow);

            double subtotal = 0.0;
            for (int i = 0; i < model.getRowCount(); i++) {
                double itemPrice = Double.parseDouble(model.getValueAt(i, 2).toString());
                int itemQuantity = Integer.parseInt(model.getValueAt(i, 1).toString());
                subtotal += itemPrice * itemQuantity;
            }

            double taxRate = 0.10;
            double tax = subtotal * taxRate;
            double total = subtotal + tax;

            subtotalfield.setText(String.valueOf(subtotal));
            taxfield.setText(String.valueOf(tax));
            totalfield.setText(String.valueOf(total));
        }
    }//GEN-LAST:event_removebuttonActionPerformed

